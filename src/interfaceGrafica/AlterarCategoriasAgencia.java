    public AlterarCategoriasAgencia(int cod) {
        initComponents();
        this.cod = cod;
        ArrayList<Categoria> categorias = new ArrayList<>();
        DefaultTableModel tabela = new DefaultTableModel();
        try {
            categorias = fachada.listarCatAg(cod);
            tabela.setColumnIdentifiers(new String[]{"Codigo", "Nome"});
            for (int i = 0; i < categorias.size(); i++) {
                Categoria categoria = new Categoria();
                categoria = fachada.procurarCat(categorias.get(i).getCat_cod());
                tabela.addRow(new Object[]{categorias.get(i).getCat_cod(), categoria.getTipo()});
            }
            jTable1.setModel(tabela);
            categorias = new ArrayList<>();
            categorias = fachada.listarCat();
            codigo = new int[categorias.size()];
            for (int i = 0; i < categorias.size(); i++) {
                jComboBox1.addItem(categorias.get(i).getTipo());
                codigo[i] = categorias.get(i).getCat_cod();
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }

